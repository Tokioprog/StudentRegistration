package esfe.utils;

// Enum que representa los tres tipos de operaciones que se pueden realizar sobre una entidad:
// Crear (CREATE), Actualizar (UPDATE) o Eliminar (DELETE)
public enum CUD {
    CREATE, // Operación para crear un nuevo registro
    UPDATE, // Operación para actualizar un registro existente
    DELETE  // Operación para eliminar un registro existente
}
